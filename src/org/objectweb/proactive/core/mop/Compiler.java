package org.objectweb.proactive.core.mop;

import java.io.File;

/**
 *	An interface that describes a Java source to Java bytecode compiler.
 *	Details about a specific compiler on a specific platform are contained
 * within a specific implementation of this interface and in ProActive properties
 */
interface Compiler {

  public void compile(File f) throws java.io.IOException;


  public void compile(String filename) throws java.io.IOException;


  public boolean needToContinue(String filename);
}

